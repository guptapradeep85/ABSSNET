# ---------- Calibration metrics + temperature scaling ----------
def ece_mce(probs, labels, n_bins=15):
    probs = np.asarray(probs, dtype=np.float64)
    labels = np.asarray(labels, dtype=int)
    conf = probs.max(1)
    pred = probs.argmax(1)
    acc = (pred == labels).astype(float)
    edges = np.linspace(0, 1, n_bins + 1)
    ece = 0.0
    mce = 0.0
    for i in range(n_bins):
        m = (conf > edges[i]) & (conf <= edges[i + 1])
        if m.sum() == 0:
            continue
        gap = abs(acc[m].mean() - conf[m].mean())
        ece += m.mean() * gap
        mce = max(mce, gap)
    return float(ece), float(mce)

def brier_multiclass(probs, labels):
    onehot = np.eye(probs.shape[1])[labels]
    return float(np.mean(np.sum((probs - onehot) ** 2, axis=1)))

def nll_score(probs, labels, eps=1e-12):
    p = probs[np.arange(len(labels)), labels]
    return float(-np.mean(np.log(np.clip(p, eps, 1.0))))

def fit_temperature_from_probs(probs, labels, max_iter=500):
    logits = np.log(np.clip(np.asarray(probs), 1e-12, 1.0))
    logits_t = torch.tensor(logits, dtype=torch.float32)
    labels_t = torch.tensor(labels, dtype=torch.long)
    T = torch.nn.Parameter(torch.ones(1))
    opt = torch.optim.LBFGS([T], lr=0.01, max_iter=max_iter)
    lossf = torch.nn.CrossEntropyLoss()
    def closure():
        opt.zero_grad()
        loss = lossf(logits_t / T.clamp_min(1e-4), labels_t)
        loss.backward()
        return loss
    opt.step(closure)
    Tval = float(T.detach().clamp_min(1e-4).item())
    scaled = np.exp(logits / Tval)
    scaled /= scaled.sum(1, keepdims=True)
    return Tval, scaled

def run_calibration_revision(eval_res, out_csv=None):
    probs = np.asarray(eval_res['y_prob'], dtype=np.float64)
    labels = np.asarray(eval_res['y_true'], dtype=int)
