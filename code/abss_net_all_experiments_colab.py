"""## 🎯 Cell 18 — ECE Calibration Reliability Diagram *(NEW)*

Bins predictions by max softmax confidence and plots mean confidence vs mean accuracy.  
**Red bars** = over-confident gaps, **Green bars** = under-confident gaps.
"""

def plot_calibration_diagram(y_true, y_prob, n_bins=10, out_path=None):
    max_conf = y_prob.max(axis=1)
    y_pred_l = y_prob.argmax(axis=1)
    correct  = (y_pred_l == y_true).astype(float)
    edges    = np.linspace(0, 1, n_bins+1)
    bc, ba, bsz = [], [], []
    for i in range(n_bins):
        msk = (max_conf>=edges[i]) & (max_conf<edges[i+1])
        bc.append(max_conf[msk].mean() if msk.sum()>0 else (edges[i]+edges[i+1])/2)
        ba.append(correct[msk].mean()  if msk.sum()>0 else 0.0)
