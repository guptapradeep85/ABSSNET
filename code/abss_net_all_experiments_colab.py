"""## 🏋️ Cell 10 — Single Fold Trainer"""

def run_fold(train_samples, val_samples, fold_id=0, epochs=EPOCHS, verbose=True):
    seed_everything(42 + fold_id)
    train_ds     = DRSplitDataset(train_samples, transform=train_transform)
    val_ds       = DRSplitDataset(val_samples,   transform=val_transform)
    train_loader = DataLoader(train_ds, batch_size=BATCH_SIZE, shuffle=True,
                              num_workers=NUM_WORKERS, pin_memory=True)
    val_loader   = DataLoader(val_ds, batch_size=BATCH_SIZE, shuffle=False,
                              num_workers=NUM_WORKERS, pin_memory=True)

    class_weights   = make_class_weights([l for _,l in train_samples])
    model           = ABSSNet(num_classes=NUM_CLASSES).to(device)
    criterion_focal = FocalLoss(gamma=2.0, alpha=class_weights)
    optimizer       = torch.optim.AdamW(model.parameters(), lr=LR, weight_decay=WEIGHT_DECAY)
    scheduler       = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=epochs)
    scaler          = torch.cuda.amp.GradScaler(enabled=torch.cuda.is_available())
    cal_metric      = MulticlassCalibrationError(num_classes=NUM_CLASSES, n_bins=15, norm='l1').to(device)

    ckpt   = os.path.join(OUT_DIR, f'abssnet_fold{fold_id}.pth')
    best_k = -1.0; history = []

    for epoch in range(epochs):
        tr  = train_one_epoch(model, train_loader, optimizer, criterion_focal, scaler)
        res = validate_one_epoch(model, val_loader, criterion_focal, cal_metric)
        scheduler.step()
        history.append({'epoch': epoch+1, 'train_loss': tr[0], 'train_acc': tr[1],
                        'train_f1': tr[2], 'train_kappa': tr[3],
                        'val_loss': res['loss'], 'val_acc': res['acc'],
                        'val_f1': res['f1'],   'val_kappa': res['kappa'],
                        'val_auc': res['auc'], 'val_ece': res['ece']})
        if verbose:
            print(f"[fold {fold_id}] ep {epoch+1:02d} | "
                  f"Acc {res['acc']:.4f} F1 {res['f1']:.4f} QWK {res['kappa']:.4f} "
                  f"AUC {res['auc']:.4f} ECE {res['ece']:.4f}")
        if res['kappa'] > best_k:
            best_k = res['kappa']
            torch.save(model.state_dict(), ckpt)

    model.load_state_dict(torch.load(ckpt, map_location=device))
    res = validate_one_epoch(model, val_loader, criterion_focal, cal_metric)
    res['history'] = pd.DataFrame(history)
    res['ckpt']    = ckpt
    return model, res

