class DRFolderDataset(Dataset):
    def __init__(self, img_dir, class_to_idx, transform=None):
        self.img_dir = img_dir
        self.class_to_idx = class_to_idx
        self.transform = transform
        self.samples = []

        valid_exts = (".png", ".jpg", ".jpeg", ".bmp", ".tif", ".tiff", ".webp")

        for class_name, class_idx in class_to_idx.items():
            class_folder = os.path.join(img_dir, class_name)

            if not os.path.isdir(class_folder):
                print(f"Warning: folder not found -> {class_folder}")
                continue

            for file_name in os.listdir(class_folder):
                file_path = os.path.join(class_folder, file_name)

                if os.path.isfile(file_path) and file_name.lower().endswith(valid_exts):
                    self.samples.append((file_path, class_idx))

        if len(self.samples) == 0:
            raise ValueError(f"No images found inside {img_dir}. Please check folder names and file extensions.")

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, idx):
        img_path, label = self.samples[idx]
        image = Image.open(img_path).convert("RGB")

        if self.transform:
            image = self.transform(image)

        return image, label


