@torch.no_grad()
def validate_one_epoch(model, loader):
    model.eval()
    running_loss = 0.0
    preds_all = []
    labels_all = []
    probs_all = []

    calibration_metric.reset()

    for images, labels in tqdm(loader, desc="Validation", leave=False):
        images = images.to(device, non_blocking=True)
        labels = labels.to(device, non_blocking=True)

        logits, confidence = model(images)
        loss_cls = criterion_focal(logits, labels)
        loss_cal = calibration_loss(logits, labels)
        loss = loss_cls + 0.1 * loss_cal

        running_loss += loss.item() * images.size(0)

        probs = torch.softmax(logits, dim=1)
        preds = torch.argmax(probs, dim=1)

        calibration_metric.update(probs, labels)

        preds_all.extend(preds.cpu().numpy())
        labels_all.extend(labels.cpu().numpy())
        probs_all.extend(probs.cpu().numpy())

    epoch_loss = running_loss / len(loader.dataset)
    epoch_acc = accuracy_score(labels_all, preds_all)
    epoch_f1 = f1_score(labels_all, preds_all, average="macro")
    epoch_kappa = cohen_kappa_score(labels_all, preds_all, weights="quadratic")
    epoch_ece = calibration_metric.compute().item()

    try:
        epoch_auc = roc_auc_score(labels_all, np.array(probs_all), multi_class="ovr")
    except Exception:
        epoch_auc = 0.0

    return epoch_loss, epoch_acc, epoch_f1, epoch_kappa, epoch_auc, epoch_ece, labels_all, preds_all, probs_all

