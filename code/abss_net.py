# 17. ABSS-Net model
# =========================
class ABSSNet(nn.Module):
    def __init__(self, num_classes=5, backbone_name="tf_efficientnet_b0_ns", pretrained=True):
        super().__init__()

        self.backbone = timm.create_model(
            backbone_name,
            pretrained=pretrained,
            features_only=True,
            out_indices=(2, 3, 4)
        )

        feature_info = self.backbone.feature_info.channels()
        c3, c4, c5 = feature_info[-3], feature_info[-2], feature_info[-1]

        self.attn3 = CBAMBlock(c3)
        self.attn4 = CBAMBlock(c4)
        self.attn5 = CBAMBlock(c5)

        self.proj3 = nn.Conv2d(c3, 128, kernel_size=1)
        self.proj4 = nn.Conv2d(c4, 128, kernel_size=1)
        self.proj5 = nn.Conv2d(c5, 128, kernel_size=1)

        self.fusion = nn.Conv2d(128 * 3, 256, kernel_size=3, padding=1)
        self.bn = nn.BatchNorm2d(256)
        self.act = nn.GELU()

        self.token_proj = nn.Conv2d(256, 256, kernel_size=1)
        self.ssm1 = StateSpaceBlock(dim=256, expansion=2, dropout=0.1)
        self.ssm2 = StateSpaceBlock(dim=256, expansion=2, dropout=0.1)

        self.cls_head = nn.Sequential(
            nn.LayerNorm(256),
            nn.Linear(256, 128),
            nn.GELU(),
            nn.Dropout(0.3),
            nn.Linear(128, num_classes)
        )

        self.conf_head = nn.Sequential(
            nn.LayerNorm(256),
            nn.Linear(256, 64),
            nn.GELU(),
            nn.Linear(64, 1)
        )

    def forward(self, x):
        feats = self.backbone(x)
        f3, f4, f5 = feats[-3], feats[-2], feats[-1]

        f3 = self.attn3(f3)
        f4 = self.attn4(f4)
        f5 = self.attn5(f5)

        f3 = self.proj3(f3)
        f4 = self.proj4(f4)
        f5 = self.proj5(f5)

        target_size = f3.shape[-2:]
        f4 = F.interpolate(f4, size=target_size, mode="bilinear", align_corners=False)
        f5 = F.interpolate(f5, size=target_size, mode="bilinear", align_corners=False)

        fused = torch.cat([f3, f4, f5], dim=1)
        fused = self.act(self.bn(self.fusion(fused)))

        tokens = self.token_proj(fused)
        B, C, H, W = tokens.shape
        tokens = tokens.flatten(2).transpose(1, 2)  # [B, N, C]

        tokens = self.ssm1(tokens)
        tokens = self.ssm2(tokens)

        pooled = tokens.mean(dim=1)

        logits = self.cls_head(pooled)
        confidence = torch.sigmoid(self.conf_head(pooled))

        return logits, confidence

