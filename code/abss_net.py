# 16. State space inspired block
# =========================
class StateSpaceBlock(nn.Module):
    def __init__(self, dim, expansion=2, dropout=0.1):
        super().__init__()
        hidden_dim = dim * expansion

        self.norm = nn.LayerNorm(dim)
        self.in_proj = nn.Linear(dim, hidden_dim)
        self.dwconv = nn.Conv1d(hidden_dim, hidden_dim, kernel_size=5, padding=2, groups=hidden_dim)
        self.gate = nn.Linear(dim, hidden_dim)
        self.out_proj = nn.Linear(hidden_dim, dim)
        self.dropout = nn.Dropout(dropout)

    def forward(self, x):
        # x: [B, N, C]
        residual = x
        x_norm = self.norm(x)

        u = self.in_proj(x_norm)              # [B, N, hidden]
        g = torch.sigmoid(self.gate(x_norm))  # [B, N, hidden]

        u = u.transpose(1, 2)                 # [B, hidden, N]
        u = self.dwconv(u)
        u = u.transpose(1, 2)                 # [B, N, hidden]

        x = u * g
        x = self.out_proj(x)
        x = self.dropout(x)

        return x + residual

